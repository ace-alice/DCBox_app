mixin FetchImage {
  Widget _getWidget(
    String name, {
    BoxFit fit = BoxFit.contain,
    double? width,
    double? height,
  }) {
    final map = imageJson[runtimeType.toString()];
    final placeholder = Placeholder(
      fallbackWidth: width ?? 0,
      fallbackHeight: height ?? 0,
    );
    if (map == null) {
      return placeholder;
    }
    final base64Image = map['${name}Image'];
    if (base64Image == null) {
      return placeholder;
    }
    try {
      final imageData = base64.decode(base64Image);
      return Image.memory(
        imageData,
        fit: fit,
        width: width,
        height: height,
        gaplessPlayback: true,
      );
    } catch (e) {
      return placeholder;
    }
  }

  Uint8List? _getImageData(String name) {
    final map = imageJson[runtimeType.toString()];
    if (map == null) {
      return null;
    }
    final base64Image = map['${name}Image'];
    if (base64Image == null) {
      return null;
    }
    try {
      return base64.decode(base64Image);
    } catch (e) {
      return null;
    }
  }

  Image? _getImage(
    String name, {
    BoxFit fit = BoxFit.contain,
    double? width,
    double? height,
  }) {
    final imageData = _getImageData(name);
    if (imageData == null) {
      return null;
    }
    try {
      return Image.memory(
        imageData,
        fit: fit,
        width: width,
        height: height,
        gaplessPlayback: true,
      );
    } catch (e) {
      return null;
    }
  }

  /// PascalCase
  ///
  /// ex: app_common_icon => AppCommonIcon
  String _snakeCaseConverterToPascalCase(String text) {
    if (text.isEmpty) {
      return text;
    }
    return text
        .split('_')
        .map((e) => e[0].toUpperCase() + e.substring(1))
        .join();
  }

  /// CamelCase
  ///
  /// ex: app_common_icon => appCommonIcon
  String _snackCaseConverterToCamelCase(String text) {
    if (text.isEmpty) {
      return text;
    }
    final pascalCase = _snakeCaseConverterToPascalCase(text);
    return pascalCase[0].toLowerCase() + pascalCase.substring(1);
  }
